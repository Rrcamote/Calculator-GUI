    private void btnEqualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEqualMouseClicked
        if (operator == "+") {
            SecondNum = txtDisplay.getText();
            double sum = Double.parseDouble(FirstNum) + Double.parseDouble(SecondNum);
            txtDisplay.setText("" + sum);
        } else if (operator == "-") {
            SecondNum = txtDisplay.getText();
            double sub = Double.parseDouble(FirstNum) - Double.parseDouble(SecondNum);
            txtDisplay.setText("" + sub);
        } else if (operator == "*") {
            SecondNum = txtDisplay.getText();
            double mult = Double.parseDouble(FirstNum) * Double.parseDouble(SecondNum);
            txtDisplay.setText("" + mult);
        } else if (operator == "/") {
            SecondNum = txtDisplay.getText();
            double div = Double.parseDouble(FirstNum) / Double.parseDouble(SecondNum);
            txtDisplay.setText("" + div);
        }
    }//GEN-LAST:event_btnEqualMouseClicked

